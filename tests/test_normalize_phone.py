"""Нормализация телефонов: все форматы, которые реально присылают в заявках."""

import pytest

from app.services.bitrix import extract_bare_phone, normalize_phone


@pytest.mark.parametrize(
    ("raw", "expected"),
    [
        ("89141234567", "+79141234567"),  # 8 в начале
        ("79141234567", "+79141234567"),  # 7 без плюса
        ("+79141234567", "+79141234567"),  # уже нормализован
        ("8 (914) 123-45-67", "+79141234567"),  # пробелы и скобки
        ("+7 914 123 45 67", "+79141234567"),
        ("8-914-123-45-67", "+79141234567"),
        ("тел. 89141234567 (Иван)", "+79141234567"),  # мусор вокруг номера
        ("+995 551 23 45 67", "+995551234567"),  # другой код страны — как есть
        # 10 цифр без кода — регион по умолчанию Россия
        # (раньше неверно получалось +9141234567)
        ("9141234567", "+79141234567"),
        ("914 123-45-67", "+79141234567"),
        # добавочный номер отбрасывается по маркеру
        ("89141234567 доб. 123", "+79141234567"),
        ("89141234567 доб 123", "+79141234567"),
        ("8 (914) 123-45-67, вн. 21", "+79141234567"),
        ("89141234567x123", "+79141234567"),
        ("89141234567#123", "+79141234567"),
        ("+1-202-555-0142 ext. 42", "+12025550142"),
        ("+1 202 555 0142 extension 7", "+12025550142"),
        # зарубежный номер 11-15 цифр — E.164 как есть
        ("+1 202 555 0142", "+12025550142"),
    ],
)
def test_normalize_valid(raw, expected):
    assert normalize_phone(raw) == expected


@pytest.mark.parametrize(
    "raw",
    [
        "123-45-67",  # короткий городской без кода
        "12345",
        "",
        None,
        "сантехника",  # цифр нет вообще
        "1234567890123456",  # 16 цифр — длиннее E.164
        "+123 4567 8901 2345 678",  # 18 цифр
    ],
)
def test_normalize_invalid(raw):
    assert normalize_phone(raw) is None


@pytest.mark.parametrize(
    ("raw", "expected"),
    [
        ("89141234567", "+79141234567"),
        ("  +7 (914) 123-45-67. ", "+79141234567"),
        ("8-914-123-45-67", "+79141234567"),
        ("89141234567 доб. 12", "+79141234567"),  # добавочный — часть номера
        ("89141234567x12", "+79141234567"),
        ("8 (914) 123-45-67, вн. 21", "+79141234567"),
        ("+1 202 555 0142", "+12025550142"),  # зарубежный с явным «+»
    ],
)
def test_extract_bare_phone_accepts_pure_numbers(raw, expected):
    assert extract_bare_phone(raw) == expected


@pytest.mark.parametrize(
    "raw",
    [
        # полная фраза с номером внутри — это НОВАЯ заявка, а не ответ-телефон
        "Мария, 89141234567, электрика, заменить розетку",
        "тел. 89141234567 (Иван)",
        "запишите 89141234567",
        "сантехника",
        "",
        None,
        "8914",  # цифры, но номером не является
        # после добавочного идёт новая заявка: терять её нельзя
        "89141234567 доб. 12, Мария, электрика, заменить розетку",
        "89141234567 доб 12 Мария",
        # номер + сумма: 15 цифр без «+» — не пятнадцатизначный телефон
        "8 (914) 123-45-67, 5000",
        "89141234567, 5000",
        "+7 (914) 123-45-67, 5000",
        "+791412345675000",
        # зарубежный номер + отдельный числовой хвост также переразбирается
        "+1 202 555 0142, 5000",
        "+995 551 23 45 67, 500",
        "+44 20 7946 0958; 1200",
        "+1 202 555 0142, (5000)",
        "+1 202 555 0142, -5000",
        "+1 202 555 0142, +5000",
        "+1 202 555 0142. 5000",
        # два номера подряд — тоже не «голый» ответ-номер
        "89141234567, 89147654321",
    ],
)
def test_extract_bare_phone_rejects_phrases(raw):
    assert extract_bare_phone(raw) is None
